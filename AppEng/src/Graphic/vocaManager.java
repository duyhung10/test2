    private void btDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeleteActionPerformed
        try {
            // TODO add your handling code here:
            String delword = tfSearch.getText();
            if(delword.equals("")){
                JOptionPane.showMessageDialog(rootPane, "Not found data");
            }
            else{
                int kqua;
                kqua = dic.deleteWord(delword);
                deleteRow(kqua);
                JOptionPane.showMessageDialog(rootPane, "Data have been delete");
                btDelete.disable();
            }
            
        } catch (IOException ex) {
            Logger.getLogger(vocaManager.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btDeleteActionPerformed

    private void btEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditActionPerformed
        try {
            // TODO add your handling code here:
            String findingWord = tfSearch.getText();
            if(findingWord.equals("")){
                JOptionPane.showMessageDialog(rootPane, "Let search your word at 'TextFieldSearch' first.");
            }
            else{
                // Đưa ra Form editWord
                Word res = dic.find(findingWord);
                editWord editWord = new editWord(dic, res.Keyword, res.Meaning, res.Category);
                editWord.show();
                
                //Xóa từ hiện tại để chuẩn bị thêm bản chỉnh sửa của từ đó
                int kqua;
                kqua = dic.deleteWord(findingWord);
                deleteRow(kqua);
                
                
            }
            
        } catch (IOException ex) {
            Logger.getLogger(vocaManager.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btEditActionPerformed

