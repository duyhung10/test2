    // Hiện các từ trong file từ vựng  ra bảng
    private void bindListWord() throws IOException{     
        ArrayList allwords = dic.getAllWords();
        
        DefaultListModel model=new DefaultListModel();
        String words = new String();
        for(int i =0; i < allwords.size();i++){
            Word word = (Word) allwords.get(i);
            
            words += (word.Keyword + "\n");
        }
        
        txListVoca.setText(words);
    }
    
    
    
    // Phương thức xóa 1 hàng.
    private void deleteRow(int index) throws FileNotFoundException, IOException{
        HSSFWorkbook wb;
        HSSFSheet worksheet;
        FileInputStream fIps = new FileInputStream(XFileVoca);

        if(fIps.available() >= 512){
            wb = new HSSFWorkbook(fIps);
            worksheet = wb.getSheetAt(0);
        }else{
            wb = new HSSFWorkbook();
            worksheet = wb.createSheet("Dictionary");
        }
        
        worksheet.removeRow(worksheet.getRow(index));
        
        
        fIps.close();
        FileOutputStream output_file =new FileOutputStream(new File(XFileVoca));  
        wb.write(output_file);
        output_file.close();
        
    }
    
